# Initializing two varibles with integer values
number1 = 10
number2 = 5

# Calculating the sum of the two numbers
sum = number1 + number2

# Calculating the difference between the two numbers
difference = number1 - number2

# Calculating the product of the two numbers
product = number1 * number2

# Printing the results of each operation
print("Addition of {} and {} is {}".format(number1, number2, sum))
print("Subtraction between {} and {} is {}".format(number1, number2, difference))
print("Multiplication of {} and {} is {}".format(number1, number2, product))